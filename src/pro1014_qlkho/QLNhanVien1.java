    public void delete() {

        try {

            int i = JOptionPane.showConfirmDialog(this, "Ban co muon xoa khong", "hoi xoa", JOptionPane.YES_NO_OPTION);
            if (i != JOptionPane.YES_OPTION) {
                return;
            }
            listnv.remove(vitri);
//            tbsv.remove(vitri);
            String sql = "delete from NHANVIEN where MaNhanVien=?";

            PreparedStatement pstm = cn.prepareStatement(sql);
            pstm.setString(1, txtMaNV.getText());
            int chon = pstm.executeUpdate();
            if (chon > 0) {
                if (listnv.size() == 0) {
                    clear();
                } else if (vitri == 0) {
                    filltable();
                    JOptionPane.showMessageDialog(this, "Xoa thanh cong");
                    display(0);
                } else {
                    filltable();
                    JOptionPane.showMessageDialog(this, "Xoa thanh cong");
                    vitri--;
                    display(vitri);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Khong xoa duoc dong nao");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Không thể xóa được nhân viên,Nhân viên vẫn đang quản lí phiếu");
        }
    }
