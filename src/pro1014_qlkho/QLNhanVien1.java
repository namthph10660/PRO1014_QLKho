    public void loadatatolist() {
        try {
            String sql = "select * from NHANVIEN";
            Statement stm = cn.createStatement();
            ResultSet rs = stm.executeQuery(sql);
            while (rs.next()) {
                String manv = rs.getString(1);
                String ten = rs.getString(2);
                String sodt = rs.getString(3);
                String CMND = rs.getString(4);
                Date NgaySinh = rs.getDate(5);
                boolean GioiTinh= rs.getBoolean(6);
                boolean vaitro= rs.getBoolean(7);
                String matkhau = rs.getString(8);
                listnv.add(new NhanVien(manv, ten, sodt, CMND, matkhau, NgaySinh, vaitro, GioiTinh));
            }
            rs.close();
            stm.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi load data to list");
        }
    }
    public void filltable() {
        try {
            model.setRowCount(0);
            for (NhanVien nv : listnv) {
                model.addRow(new Object[]{nv.getManv(), nv.getHoten(), (nv.isGioitinh() ? "Nam" : "Nữ"), nv.getSodienthoai(),nv.getCMND(),nv.getNgaySinh()});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi fill data to table");
        }
    }
    public void display(int vitri) {
        txtMaNV.setText(tbl_DSachNV.getValueAt(vitri, 0).toString());
        txtTenNV.setText(tbl_DSachNV.getValueAt(vitri, 1).toString());
        txtSDT.setText(tbl_DSachNV.getValueAt(vitri, 3).toString());
        txtCMND.setText(tbl_DSachNV.getValueAt(vitri, 4).toString());
        txtNgaySinh.setText(tbl_DSachNV.getValueAt(vitri, 5).toString());
        tfmaphu.setText(tbl_DSachNV.getValueAt(vitri,0).toString());
        String gioitinh = tbl_DSachNV.getValueAt(vitri, 2).toString();
        if (gioitinh.equalsIgnoreCase("Nam")) {
            rdoNam.setSelected(true);
        } else if (gioitinh.equalsIgnoreCase("Nữ")) {
            rdoNu.setSelected(true);
        }
         tbl_DSachNV.setRowSelectionInterval(vitri, vitri);

    }
