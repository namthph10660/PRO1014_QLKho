    public boolean check() {
        for (NhanVien nv : listnv) {
            if (txtMaNV.getText().trim().equalsIgnoreCase(nv.getManv())) {
                JOptionPane.showMessageDialog(this, "mã nhân viên đã tồn tại ");
                return false;
            }
        }
        return true;
    }
    public void them(){
        try {
            String ma = txtMaNV.getText();
            String ten = txtTenNV.getText();
            String sdt = txtSDT.getText();
            String cmnd= txtCMND.getText();
            Date ngaysinh = Date.valueOf(txtNgaySinh.getText());
            boolean gioitinh;
            if (rdoNam.isSelected()) {
                gioitinh = true;
            } else {
                gioitinh= false;
            }
            listnv.add(new NhanVien(ma, ten, sdt, cmnd, ngaysinh, gioitinh));
            model.addRow(new Object[]{ma, ten, gioitinh ? "Nam" : "Nữ",sdt,cmnd,ngaysinh});
            String sql = "insert into NHANVIEN(MaNhanVien,Hoten,SoDthoai,CMND,NgaySinh,GioiTinh)\n"
                    + "values(?,?,?,?,?,?)";
             PreparedStatement pstm = cn.prepareStatement(sql);
            pstm.setString(1,ma);
            pstm.setString(2,ten);
            pstm.setString(3,sdt);
            pstm.setString(4,cmnd);
            pstm.setDate(5,ngaysinh);
            pstm.setBoolean(6,gioitinh);
            int i = pstm.executeUpdate();
            if (i > 0) {
                JOptionPane.showMessageDialog(this, "Them dl thanh cong");

                vitri = listnv.size() - 1;
                display(vitri);
            } else {
                JOptionPane.showMessageDialog(this, "Ban khong them duoc dong nao");

            }
            
        } catch (Exception evt) {
            JOptionPane.showMessageDialog(this, evt);
        }
        
    } 
